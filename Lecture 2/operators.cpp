#include <iostream>
using namespace std;
int main()
{
    float a = 5.0, b = 8.0;
    cout << "arithmetic operators" << endl; //Arithmetic Operators
    cout << "addition of a and b: " << a + b << endl;
    cout << "subtraction of a and b: " << a - b << endl;
    cout << "multiplication of a and b: " << a * b << endl;
    cout << "division of a and b: " << a / b << endl;

    return 0;
}